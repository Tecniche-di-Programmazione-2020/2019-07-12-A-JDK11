package it.polito.tdp.food.model;

public class Arco {
	Food alimento1;
	Food alimento2;
	
	public Arco(Food alimento1, Food alimento2) {
		super();
		this.alimento1 = alimento1;
		this.alimento2 = alimento2;
		
	}
	public Food getAlimento1() {
		return alimento1;
	}
	public Food getAlimento2() {
		return alimento2;
	}
	
	
	

}
